### STRINGS ###

## Concatenation ##

str1 = "Hello"
str2 = "World!"
concatenated = str1 + " " + str2
print(concatenated)  # Output: Hello World!

## Indexing ## 
text = "Python"
print(text[0])  # Output: P
print(text[2])  # Output: t
print(text[-1])  # Output: n


## Slicing ##
phrase = "Hello, World!"
print(phrase[0:5])  # Output: Hello
print(phrase[7:])  # Output: World!
print(phrase[:5])  # Output: Hello
print(phrase[-6:-1])  # Output: World
 