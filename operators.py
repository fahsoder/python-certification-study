### Arithmetic operators ###
number_1 = 5
number_2 = 2

sum = number_1 + number_2
sub = number_1 - number_2
multi = number_1 * number_2
division = number_1 / number_2
full_division = number_1 // number_2
module = number_1 % number_2
expo = number_1 ** number_2

print(sum) # 7
print(sub) # 3
print(multi)  # 10
print(division) # 2.5
print(full_division) # 2
print(module)  # 1
print(expo) # 25


i = 'teste'
item = [i]
### Identity operators ###
# is / is not
print(i is 'teste')
print(i is not 'teste')

# in / not in
print(i in item)
print(i not in item)